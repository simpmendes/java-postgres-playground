package com.example;

public class Marca {
    private Long id;
    private String nome;
    
    public void setId(Long id) {
        this.id = id;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public Long getId() {
        return id;
    }
    public String getNome() {
        return nome;
    }
}
